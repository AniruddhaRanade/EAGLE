@implementation UIColor(Extensions)


// -------------------------------------------------------------------------------------------------

+ (UIColor *)bluetoothBlue {
	return [UIColor colorWithRed:67 / 255.0f
						   green:132 / 255.0f
                            blue:248 / 255.0f
                           alpha:1.0f];    
}

// -------------------------------------------------------------------------------------------------

+ (UIColor *)bluetoothBlueWithAlpha {
	return [UIColor colorWithRed:67 / 255.0f
						   green:132 / 255.0f
                            blue:248 / 255.0f
                           alpha:0.5f];    
}

@end